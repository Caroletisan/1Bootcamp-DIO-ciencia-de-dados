#Interfaces definem um contrato, onde são declarados os métodos, ou seja, o que uma classe deve fazer e não como e suas respectivas assinaturas.
# Em python utilizamos classes abstratas para criar contratos e as claasses abstratas não podem ser instanciadas.  

# ABC(Abstract Basic Class) é um módulo fornecido pelo python para definir as classes abstratas. 
# Funciona decorando métodos da classe base como abstratos e, em seguida, registrando classes concretas como implementações da base abstrata.

from abc import ABC, abstractmethod, abstractproperty

class ControleRemoto(ABC):
    @abstractmethod
    def ligar(self):
        pass

    @abstractmethod
    def desligar(self):
        pass

    @property
    @abstractproperty
    def marca(self):
        pass


class ControleTV(ControleRemoto):
    def ligar(self):
        print("Ligando a TV...")
        print("Ligada!")

    def desligar(self):
        print("Desligando a TV...")
        print("Desligada!")

    @property
    def marca(self):
        return "Philco"


class ControleArCondicionado(ControleRemoto):
    def ligar(self):
        print("Ligando o Ar Condicionado...")
        print("Ligado!")

    def desligar(self):
        print("Desligando o Ar Condicionado...")
        print("Desligado!")

    @property
    def marca(self):
        return "LG"


controle = ControleTV()
controle.ligar()
controle.desligar()
print(controle.marca)


controle = ControleArCondicionado()
controle.ligar()
controle.desligar()
print(controle.marca)
